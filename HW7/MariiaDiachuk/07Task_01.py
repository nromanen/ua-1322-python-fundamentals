def largest_number(num1, num2):
    """
    Returns the largest of two numbers.

    Parameters:
    num1 (int or float): The first number.
    num2 (int or float): The second number.

    Returns:
    int or float: The larger of the two numbers.
    """
    if num1 > num2:
        return num1
    else:
        return num2
